### PYTHON CASTING ###

## int
a = int(3)
b = int(12.4) # 12
c = int(12.8) # 12
d = int('5')
print(a, "\t", b, "\t", c, "\t", d)

## float
e = float(3)
f = float(12.4)
g = float(12.8)
h = float('5')
print(e, "\t", f, "\t", g, "\t", h)

## str
i = str(3) 
j = str(12.4)
k = str(12.8)
l = str('5')
print(i, "\t", j, "\t", k, "\t", l)
