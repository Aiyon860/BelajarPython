### VARIABLE NAMES ###

## Legal variable names:
myvar = "Daniel"
my_var = "Daniel"
_my_var = "Daniel"
myVar = "Daniel"
MYVAR = "Daniel"
MyVar = "Daniel"
My_Var = "Daniel"
myvar2 = "Daniel"

## Illegal variable names:
# 2myvar = "Daniel"
# my-var = "Daniel"
# my var = "Daniel"

## Multi words variable names:
# camelCase
minumanMekdi = "Coca Cola Float"
print("Saya pesen minum", minumanMekdi, "di mekdi ngaliyan")

# PascalCase
makananMekdi = "Spicy Chicken"
print("Dia pesen 2", makananMekdi, "sekaligus")

# snake_case
happy_meal = "Small Nuggets"
print("Anak-anak disitu senang memakan", happy_meal)